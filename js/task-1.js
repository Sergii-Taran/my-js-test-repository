function calcBMIWithMessage(weight, height) {
  const w = parseFloat(weight.replace(',', '.'));
  const h = parseFloat(height.replace(',', '.'));

  if (isNaN(w) || isNaN(h) || h < 0) {
    return 'Incorrect data!';
  }

  const bmi = +(w / h ** 2).toFixed(1);

  let category;
  if (bmi < 18.5) category = 'underweightüòí';
  else if (bmi < 25) category = 'normüëå';
  else if (bmi < 30) category = 'overweightüò¢';
  else category = 'adiposityüò®';

  return `Your BMI - ${bmi} (${category})`;
}

